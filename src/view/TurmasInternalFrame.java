    private void AddTurmaBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_AddTurmaBtnActionPerformed
        String nome = nomeField.getText();
        String ano = anoField.getSelectedItem().toString();

        if (! "".equals(nome) ) {
            if (TurmaController.Salvar(nome, ano)) {
                nomeField.setText("");
                updateTableModel("");
            }
            else { JOptionPane.showMessageDialog(null, " Não foi possível salvar "+ nome +". Tente novamente"); }
        }
        else { JOptionPane.showMessageDialog(null, "Nome em branco. ", "Atenção", JOptionPane.WARNING_MESSAGE); }
    }//GEN-LAST:event_AddTurmaBtnActionPerformed

    private void DelTurmaBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_DelTurmaBtnActionPerformed
        int idx[] = TurmaTable.getSelectedRows();
        if (idx.length > 0) {
            int response = JOptionPane.showConfirmDialog(null, "Apagar "+ TurmaTable.getValueAt(TurmaTable.getSelectedRow(), 1) +"?", "Atenção",
                JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE);
            if (response == JOptionPane.YES_OPTION) {

                    int id = Integer.valueOf( TurmaTable.getValueAt( TurmaTable.getSelectedRow() , 0 ).toString() );
                    System.out.println("Botão mandou ID "+ id);
                    if (TurmaController.Apagar(id)) {
                        updateTableModel("");
                    }
                    else { JOptionPane.showMessageDialog(null, "Não foi possível excluir. Esta Turma possui Pessoas cadastradas! ", "Atenção", JOptionPane.WARNING_MESSAGE); }

            } 
        }
        else { DelTurmaBtn.setEnabled(false); }
        
        
    }//GEN-LAST:event_DelTurmaBtnActionPerformed

