    private void TurmaTableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_TurmaTableMouseClicked
        int idx[] = TurmaTable.getSelectedRows();
        if (idx.length > 0) {
            AttTurmaBtn.setEnabled(true);
            DelTurmaBtn.setEnabled(true);
        }
        else {
            AttTurmaBtn.setEnabled(false);
            DelTurmaBtn.setEnabled(false);
        }
    }//GEN-LAST:event_TurmaTableMouseClicked

    private void AttTurmaBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_AttTurmaBtnActionPerformed
        int idx[] = TurmaTable.getSelectedRows();
        if (idx.length > 0) {
            JTextField nome = new JTextField();
            JComboBox<TurmaEnum> ano = new JComboBox();
            ano.setModel(new DefaultComboBoxModel( TurmaEnum.values() ));

            Object[] message = {
                "Nome:", nome,
                "Ano:", ano
            };
            int id = Integer.valueOf( TurmaTable.getValueAt( TurmaTable.getSelectedRow() , 0 ).toString() );
            Turma ex = TurmaController.Pegar(id);
            nome.setText( ex.getNome() );
            ano.setSelectedItem( TurmaEnum.get( ex.getAno() ) );

            int option=1;
            while (option != JOptionPane.OK_CANCEL_OPTION) {
                option = JOptionPane.showConfirmDialog(null, message, "Alterar turma", JOptionPane.OK_CANCEL_OPTION);
                if (option == JOptionPane.OK_OPTION) {  
                    
                    Turma t = new Turma(ex.getId_turma(), nome.getText(), ano.getSelectedItem().toString() );
                    if (! ex.equals(t)) {
                        if (TurmaController.Alterar(t)) {
                            updateTableModel("");
                            option = OK_CANCEL_OPTION;
                        }                     
                    }  
                    else 
                        option = OK_CANCEL_OPTION;                      
                }
                else option = OK_CANCEL_OPTION;

            }
        }
        else {AttTurmaBtn.setEnabled(false);}
    }//GEN-LAST:event_AttTurmaBtnActionPerformed

